package reservation;

public class Reservation {
	
	protected String RsMemberName;
	protected String RsMemberTel;
	
	public Reservation() {
	}

	public String getRsMemberName() {
		return RsMemberName;
	}

	public void setRsMemberName(String rsMemberName) {
		RsMemberName = rsMemberName;
	}

	public String getRsMemberTel() {
		return RsMemberTel;
	}

	public void setRsMemberTel(String rsMemberTel) {
		RsMemberTel = rsMemberTel;
	}

	
}
